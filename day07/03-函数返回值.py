# 求2个数的和，并返回

# 函数的返回值
"""
def 函数名([param1, param2, ...]):
    函数体
    return 函数要返回的结果（表达式）
"""
"""
说明：
1. 返回函数的运行结果
2. return后的代码不执行
"""


def add1(a, b):
    print(a + b)


def add2(a, b):
    return a + b  # 1 + 2  = 3
    print(11111)


print(add2(1, 2))  # print(3)

result = add2(1, 2)  # result = 3
print(result)
